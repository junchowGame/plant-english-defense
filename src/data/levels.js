function colorCard(id, label, x, y, assetId) {
  return { id, label, kind: "color-card", x, y, assetId };
}

function wordCard(id, label, x, y) {
  return { id, label, kind: "word", x, y, assetId: `word_${id}` };
}

function numberCard(id, label, x, y, assetId) {
  return { id, label, kind: "number-card", x, y, assetId };
}

function sunStack(id, count, x, y) {
  return {
    id,
    label: `${count} sun${count > 1 ? "s" : ""}`,
    kind: "sun",
    x,
    y,
    assetId: "obj_sun",
    count,
  };
}

